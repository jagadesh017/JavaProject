package oopsConcepts.overriding;

public class OverRidingParent {

	public void display() {
		System.out.println("this is parent class and display method");
	}

	public void show() {
		System.out.println("this is parent class and show method");
	}

	public void add(int a, int b) {
		int c = a + b;
		System.out.println("parent c value is " + c);
	}
	
	public void keerthi() {
		System.out.println("my self keethi i am in parent class");
	}

}
